
class DayPeriod {
  constructor(year, month, day, wakeupHour, wakeupMin, workout, times, titles, total) {
    this.year = year;
    this.month = month;
    this.day = day;
    this.wakeupHour = wakeupHour;
    this.wakeupMin = wakeupMin;
    this.workout = workout;
    this.times = times;
    this.titles = titles;
    this.total = total;
  }
}


